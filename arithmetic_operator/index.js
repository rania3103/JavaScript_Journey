let students = 40;
students += 1; // same as students = students + 1;
console.log(students);

students -= 1; // same as students = students - 1;
console.log(students);

students *= 2; // same as students = students * 2;
console.log(students);

students /= 2; // same as students = students / 2;
console.log(students);

students **= 2; // same as students = students ** 2;
console.log(students);

students %= 2; // same as students = students % 2;
console.log(students);

//increment by one
let number = 4;
number++;
console.log(number);
//deincrement by one
number--;
console.log(number);